# 5- Informar três números inteiros e imprimir a média.

numero1 = int(input('Digite 1 numero'))
numero2 = int(input('Digite outro numero'))
numero3 = int(input('Por fim, digite mais um número'))

media = int((numero1 + numero2 + numero3) / 3)
print (" A media dos 3 números que voce digitou é", media) 
